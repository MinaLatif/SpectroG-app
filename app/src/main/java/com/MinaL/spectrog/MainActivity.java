        NavigationUI.setupActionBarWithNavController(this, navController, mAppBarConfiguration);
        NavigationUI.setupWithNavController(navigationView, navController);

        testLineChart=(LineChart) findViewById(R.id.line_chart);
        LineDataSet lineDataSet1 = new LineDataSet(dataValue1(),"Data Set 1");
        ArrayList<ILineDataSet> dataSets = new ArrayList<>();
        dataSets.add(lineDataSet1);

        LineData data = new LineData(dataSets);

        testLineChart.setTouchEnabled(false);
        testLineChart.setDragEnabled(false);
        testLineChart.setScaleEnabled(false);
        testLineChart.setDrawGridBackground(false);
        testLineChart.setPinchZoom(false);

        testLineChart.setBackgroundColor(Color.WHITE);

